package com.hillel.language.basis;

/**
 * Simple class that demonstrates variety of java build-in types.
 */
public class VariableTypes {

    public static void main(String[] args) {
        // numeric
        byte b = 0; // 8 bit
        short s = 0; // 16 bit
        int i = 0; // 32 bit
        long l = 0; // 64 bit

        // float point
        float f = 0.0F; // 32 bit
        double d = 0.0; // 64 bit

        // logic
        boolean bool = false;

        // character
        char c = 's';
    }
}
